<h1>BASIC RULES OF FUTSAL</h1>

<ul>
    <li>5 players versus 5 players, one of which will be a Goalkeeper. It is expected that each player will have to take a turn being Goalkeeper. SFUSA coaches will organize this</li>
    <li>No offsides</li>
    <li>No goalkeeper punts, ball must stay below head height, otherwise possession and a free kick will be awarded to the opposing team</li>
    <li>No slide tackles, players must not go to ground to steal possession back</li>
    <li>No Throw-ins from the sidelines. Instead ball is placed on ground and passed in to a teammate with the foot
</li>
    <li>Free kicks and corners the opposing team has to be minimum of 5 yards away from the ball
</li>
    <li>With a penalty kick, all players behind the ball except for the penalty kicker</li>
</ul>
